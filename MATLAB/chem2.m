function A = chem2(y1,y2,y3,k);
i=1;
dt=0.1;
E=eps;
y4=0;
y5=0;
k1=k(1);
k2=k(2);
k3=k(3);
k4=k(4);
A(1,1)=y1;
A(2,1)=y2;
A(3,1)=y3;
A(4,1)=y4;
A(5,1)=y5;
while A(1,i)>E && A(2,i)>E && A(3,i)>E;
    A(1,i+1)=A(1,i)+(-k1*A(1,i)*A(2,i)*A(3,i)+k2*A(4,i))*dt;
    A(2,i+1)=A(2,i)+(-k1*A(1,i)*A(2,i)*A(3,i)+(k2+k4)*A(4,i))*dt;
    A(3,i+1)=A(3,i)+(-k1*A(1,i)*A(2,i)*A(3,i)+(k2+k3)*A(4,i))*dt;
    A(4,i+1)=A(4,i)+(k1*A(1,i)*A(2,i)*A(3,i)-k2*A(4,i)-k3*A(4,i)-k4*A(4,i))*dt;
    A(5,i+1)=A(5,i)+((k3+k4)*A(4,i))*dt;
    i=i+1; 
end